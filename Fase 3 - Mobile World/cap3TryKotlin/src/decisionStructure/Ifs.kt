package decisionStructure

fun main() {
    var age = 10

    if (age >= 18) {
        println("You are an adult")
    } else {
        println("You are a minor")
    }

    var color = "red"
    if (color == "blue") {
        println("The color is blue")
    } else if (color == "green") {
        println("The color is green")
    } else {
        println("The color is red")
    }
}